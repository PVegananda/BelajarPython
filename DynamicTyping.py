#Dynamic Typing
"""
yakni bahasa pemrograman yang hanya mengetahui tipe variabel saat program 
berjalan dan dilakukan assignment. Tentu saja, pada Python juga umumnya 
tidak ada deklarasi variabel, hanya berupa assignment statement. Cara ini 
adalah salah satu bentuk simplifikasi alokasi memori dalam bahasa Python.
Anda dapat selalu memeriksa tipe variabel yang digunakan dengan fungsi 
type() dan memastikan tipe variabel yang tepat dengan metode isinstance(). 
"""

sebuah_variabel = 6
print(type(sebuah_variabel))

sebuah_variabel = "hallo"
print(type(sebuah_variabel))

